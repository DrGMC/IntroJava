package WeekThree;

public class BlockIfStatements {

public static void main(String[] args) {

	 { 
	        String stingofwords = "THIS IS SO COOL "; 
	        int y = 3; 
	  
	        // if block 
	        if (y == 4) {
	            y++; 
	            System.out.println(stingofwords); 
	        }
	  
	        // Executed by default 
	        System.out.println("the original value of y is = " + y); 
	    } 
	} 
}